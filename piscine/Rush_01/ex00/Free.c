#include <stdlib.h>

int	ft_free(int **cl) //Anvoets
{
	int c1;
	int c2;

	c1 = 7;
	c2 = 6;
	while (cl[1][0] != 0)
	{
		if (cl[c1][c2])
			c1--;
		else
		{
		cl[c1][c2] = 0;
		c2--;
		}
	}
	return (0);
}
