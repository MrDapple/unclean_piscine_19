char	*ft_strlowcase(char *str)
{
	int	cnt;

	cnt = 0;
	while (str[cnt] != '\0')
	{
		if (str[cnt] >= 'A' && str[cnt] <= 'Z')
			str[cnt] += 32;
		cnt++;
	}
	return (str);
}
